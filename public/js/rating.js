var rating_stats = function(myRatingVal, colorRating) {
  var myCircle = Circles.create({
    id:                  'circles-1',
    radius:              60,
    value:               myRatingVal,
    maxValue:            5,
    width:               8,
    colors:              ['#ffe6e6', colorRating],
    duration:            400,
    wrpClass:            'circles-wrp',
    textClass:           'circles-text',
    valueStrokeClass:    'circles-valueStroke',
    maxValueStrokeClass: 'circles-maxValueStroke',
    styleWrapper:        true,
    styleText:           true
  });
}

